void arithmetic() {
  int a = 10;
  int b = 3;

  print(a + b);
  print(a - b);
  print(a / b);
  print(a * b);
  print(a % b);
}

void comparison() {
  int a = 50;
  int b = 20;

  print(a == b);
  print(a != b);
  print(a <= b);
  print(a >= b);
  print(a > b);
  print(a < b);
}

void main() {
  arithmetic();
  comparison();
}
