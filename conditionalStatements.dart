// ? if statement

void ifStatement() {
  int age = 18;
  if (age >= 18) {
    print("Adult");
  }
}

// ^ if-else statement
void ifelseStatement() {
  bool hasId = true;
  if (hasId) {
    print("Enter in the club");
  } else {
    print("Not allowed to enter in the club");
  }
}

void main() {
  ifStatement();
  ifelseStatement();
}
